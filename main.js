var OhMyPassPlugin = class extends import_obsidian8.Plugin {
  constructor() {
    super(...arguments);
    this.store = new PasswordStore(this);
    this.autoLockInterval = null;
  }
  async onload() {
    await this.store.load();
    this.registerView(VIEW_TYPE_PASSWORD_LIST, (leaf) => {
      return new PasswordListView(leaf, this.store, () => this.handleUnlock());
    });
    this.addCommand({
      id: "open-password-vault",
      name: "\u6253\u5F00\u5BC6\u7801\u5E93",
      callback: () => this.activateView()
    });
    this.addCommand({
      id: "add-password",
      name: "\u6DFB\u52A0\u65B0\u5BC6\u7801",
      callback: () => {
        if (!this.store.isUnlocked()) {
          new import_obsidian8.Notice("\u8BF7\u5148\u89E3\u9501\u5BC6\u7801\u5E93");
          return;
        }
        new AddEditModal(this.app, this.store, () => this.refreshView()).open();
      }
    });
    this.addCommand({
      id: "unlock-vault",
      name: "\u89E3\u9501\u5BC6\u7801\u5E93",
      callback: () => this.handleUnlock()
    });
    this.addCommand({
      id: "lock-vault",
      name: "\u9501\u5B9A\u5BC6\u7801\u5E93",
      callback: () => {
        this.store.lock();
        this.refreshView();
        new import_obsidian8.Notice("\u5BC6\u7801\u5E93\u5DF2\u9501\u5B9A");
      }
    });
    this.addSettingTab(new OhMyPassSettingTab(this.app, this));
    this.addRibbonIcon("lock", "OhMyPass", () => {
      this.activateView();
    });
    this.autoLockInterval = window.setInterval(() => {
      if (this.store.isUnlocked() && this.store.shouldAutoLock()) {
        this.store.lock();
        this.refreshView();
        new import_obsidian8.Notice("\u5BC6\u7801\u5E93\u56E0\u8D85\u65F6\u5DF2\u81EA\u52A8\u9501\u5B9A");
      }
    }, 3e4);
    this.registerInterval(this.autoLockInterval);
  }
  onunload() {
    this.store.lock();
    if (this.autoLockInterval) {
      clearInterval(this.autoLockInterval);
    }
  }
  /** 激活密码库视图 */
  async activateView() {
    const { workspace } = this.app;
    let leaf = null;
    const leaves = workspace.getLeavesOfType(VIEW_TYPE_PASSWORD_LIST);
    if (leaves.length > 0) {
      leaf = leaves[0];
    } else {
      leaf = workspace.getRightLeaf(false);
      if (leaf) {
        await leaf.setViewState({
          type: VIEW_TYPE_PASSWORD_LIST,
          active: true
        });
      }
    }
    if (leaf) {
      workspace.revealLeaf(leaf);
    }
    if (this.store.isFirstTime()) {
      this.handleSetup();
    } else if (!this.store.isUnlocked()) {
      this.handleUnlock();
    }
  }
  /** 首次设置主密码 */
  handleSetup() {
    new SetupModal(this.app, async (password) => {
      await this.store.setup(password);
      new import_obsidian8.Notice("\u4E3B\u5BC6\u7801\u8BBE\u7F6E\u6210\u529F\uFF01");
      this.refreshView();
    }).open();
  }
  /** 解锁密码库 */
  handleUnlock() {
    if (this.store.isFirstTime()) {
      this.handleSetup();
      return;
    }
    new UnlockModal(this.app, async (password) => {
      const success = await this.store.unlock(password);
      if (success) {
        new import_obsidian8.Notice("\u5BC6\u7801\u5E93\u5DF2\u89E3\u9501");
        this.refreshView();
      }
      return success;
    }).open();
  }
  /** 刷新视图 */
  refreshView() {
    const leaves = this.app.workspace.getLeavesOfType(VIEW_TYPE_PASSWORD_LIST);
    for (const leaf of leaves) {
      const view = leaf.view;
      if (view instanceof PasswordListView) {
        view.render();
      }
    }
  }
};
